    private void btnUpdateFutsalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateFutsalActionPerformed
        // TODO add your handling code here:
        java.util.Date tglBooking = dcBookingFutsal.getDate();
        java.util.Date tglMain = dcMainFutsal.getDate();
        String kasir = txtKasirFutsal.getText();
        String penyewa = txtPenyewaFutsal.getText();
        String Status = null;
        String lapangan = cbLapanganFutsal.getSelectedItem().toString();
        int harga = Integer.parseInt(lblHargaFutsal.getText());
        int lama = Integer.parseInt(txtLamaFutsal.getText());
        String masuk = txtMasukFutsal.getText();
        String keluar = txtKeluarFutsal.getText();
        String Keterangan = null;
        if(rbMemberFutsal.isSelected()){
            Status = "Member";
        }else if(rbNonmemberFutsal.isSelected()){
            Status = "Non Member";
        }
        if(rbLunasFutsal.isSelected()){
            Keterangan = "Lunas";
        }else if(rbDPFutsal.isSelected()){   
            Keterangan = "Belum Lunas";  
        }
        Futsal futsal = new Futsal(harga, lama, lapangan, Status, kasir, penyewa, masuk, keluar,Keterangan, new java.sql.Date(tglMain.getTime()), new java.sql.Date(tglBooking.getTime()));
        int Total = futsal.Total();
        int DP = 0;
        int sisa = 0;
        if(dcBookingFutsal.getDateFormatString().equals("") || txtKasirFutsal.getText().equals("") || txtPenyewaFutsal.getText().equals("") || lblHargaFutsal.getText().equals("") || dcMainFutsal.getDateFormatString().equals("") || txtLamaFutsal.getText().equals("") ||(!rbMemberFutsal.isSelected() && !rbNonmemberFutsal.isSelected()) || (!rbDPFutsal.isSelected() && !rbLunasFutsal.isSelected())
            ){
            JOptionPane.showMessageDialog(null, "Data tidak boleh ada yang kosong");
        }else{
            if(rbMemberFutsal.isSelected()){
                Total = futsal.Total() - (20000*lama);
            }
            if(rbLunasFutsal.isSelected()){
                sisa = 0;
                lblTotalFutsal.setText("Total : Rp."+Total);
                DP = Total;
            }else if(rbDPFutsal.isSelected()){
                DP = Integer.parseInt(txtDPFutsal.getText());
                sisa = Total - Integer.parseInt(txtDPFutsal.getText());
                lblTotalFutsal.setText("Total : Rp."+Total);
            } 
            try{
                int jawab;
                if((jawab = JOptionPane.showConfirmDialog(null, "Yakin Ingin Mengupdate Data?", "konfirmasi", JOptionPane.YES_NO_OPTION)) == 0){
                    st = cn.createStatement();
                    if(tblFutsal.getSelectedRow() != -1) {
                            futsal.update(DP, Total, sisa, (String) tabmodel.getValueAt(tblFutsal.getSelectedRow(), 0));
                            loadTabelFutsal();
                            loadTabelFutsal2();
                            clear();
                    }else if(tblFutsall.getSelectedRow() != -1) {
                            futsal.update(DP, Total, sisa,(String) tabmodel2.getValueAt(tblFutsall.getSelectedRow(), 0));
                            loadTabelFutsal();
                            loadTabelFutsal2();
                            clear();
                    }
                }
            }catch(Exception e){
                e.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_btnUpdateFutsalActionPerformed

