    private void saveBadminton() {
        String kd_booking = null;
        java.util.Date tglBooking = dcBookingBadminton.getDate();
        java.util.Date tglMain = dcMainBadminton.getDate();
        String kasir = txtKasirBadminton.getText();
        String penyewa = txtPenyewaBadminton.getText();
        String Status = null;
        String lapangan = cbLapanganBadminton.getSelectedItem().toString();
        int harga = Integer.parseInt(lblHargaBadminton.getText());
        int lama = Integer.parseInt(txtLamaBadminton.getText());
        String masuk = txtMasukBadminton.getText();
        String keluar = txtKeluarBadminton.getText();
        String Keterangan = null;
        if(rbMemberBadminton.isSelected()){
            Status = "Member";
        }else if(rbNonmemberBadminton.isSelected()){
            Status = "Non Member";
        }
        if(rbLunasBadminton.isSelected()){
            Keterangan = "Lunas";
        }else if(rbDPBadminton.isSelected()){   
            Keterangan = "Belum Lunas";  
        }
        Badminton badminton = new Badminton(harga, lama, lapangan, Status, kasir, penyewa, masuk, keluar,Keterangan, new java.sql.Date(tglMain.getTime()), new java.sql.Date(tglBooking.getTime()));
        int Total = badminton.Total();
        int DP = Total;
        int sisa = 0;
        if(dcBookingBadminton.getDateFormatString().equals("") || txtKasirBadminton.getText().equals("") || txtPenyewaBadminton.getText().equals("") || lblHargaBadminton.getText().equals("") || dcMainBadminton.getDateFormatString().equals("") || txtLamaBadminton.getText().equals("") ||(!rbMemberBadminton.isSelected() && !rbNonmemberBadminton.isSelected()) || (!rbDPBadminton.isSelected() && !rbLunasBadminton.isSelected())
            ){
            JOptionPane.showMessageDialog(null, "Data tidak boleh ada yang kosong");
        }else{
            if(rbMemberBadminton.isSelected()){
                Total = badminton.Total() - (8000*lama);
            }
            if(rbLunasBadminton.isSelected()){
                sisa = 0;
                lblTotalBadminton.setText("Total : "+Total);
                DP = Total;
            }else if(rbDPBadminton.isSelected()){
                DP = Integer.parseInt(txtDPBadminton.getText());
                sisa = Total - Integer.parseInt(txtDPBadminton.getText());
                lblTotalBadminton.setText("Total : Rp."+Total);
            }
            
            badminton.generateKode(kd_booking);
            badminton.simpan(DP,Total, sisa);
        }
        
    }
