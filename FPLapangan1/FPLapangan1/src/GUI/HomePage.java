    private void btnCari1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCari1ActionPerformed
        // TODO add your handling code here:
        String kode = lblKodeBooking1.getText()+txtKodeBooking1.getText();
        if(txtKodeBooking1.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Kode Booking tidak boleh kosong");
        }else{
            try{
                st = cn.createStatement();
                rs = st.executeQuery("SELECT kd_booking, tgl_main, penyewa, masuk, keluar FROM tb_booking_futsal WHERE kd_booking = '" + kode +"'"
                   + "union all SELECT kd_booking, tgl_main, penyewa, masuk, keluar FROM tb_booking_badminton WHERE kd_booking = '" + kode +"'"
                   + "union all SELECT kd_booking, tgl_main, penyewa, masuk, keluar FROM tb_booking_voli WHERE kd_booking = '" +kode +"'");
                if(!rs.next()){
                    JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan");
                    dcBookingCari.setDate(null);
                    dcMainCari.setDate(null);
                    lblNamaCari.setText("");
                    lblSisaCari.setText("");
                }else{
                  rs.beforeFirst();
                    while (rs.next()){
                        if(kode.equals(rs.getString("kd_booking"))) {
                            java.sql.Date mainDate = rs.getDate("tgl_main");
                            dcMainCari1.setDate(new java.util.Date(mainDate.getTime()));
                            lblNamaCari1.setText(rs.getString("penyewa"));
                            txtMasukCari1.setText(rs.getString("masuk"));
                            txtKeluarCari1.setText(rs.getString("keluar"));
                        }
                    }  
                }
                
            } catch(Exception a) {
                a.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnCari1ActionPerformed

